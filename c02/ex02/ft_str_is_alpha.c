int		ft_str_is_alpha(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (str[i] < 65 || str[i] > 122 || (str[i] >= 91 && str[i] <= 96))
		{
			return (0);
		}
		i++;
	}
	return (1);
}
