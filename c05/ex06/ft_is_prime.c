int		ft_is_prime(int nb)
{
	int i;

	i = 3;
	if (nb <= 1)
		return (0);
	else if (nb == 2)
		return (1);
	else if (nb % 2 == 0)
		return (0);
	else
	{
		while (i <= nb - 1)
		{
			if (nb % i == 0)
				return (0);
			else
				i = i + 2;
		}
		return (1);
	}
}
